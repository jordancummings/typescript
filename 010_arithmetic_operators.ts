// +, -, *, /, %, ++, --

var numOne : number = 15;
var numTwo : number = 5;

// Addition
console.log("Addition:");
console.log(numOne + numTwo);

// Subtraction
console.log("Subtraction:");
console.log(numOne - numTwo);

// Multiplaction
console.log("Multiplication:");
console.log(numOne * numTwo);

// Division
console.log("Division:");
console.log(numOne / numTwo);

// Modulus
console.log("Modulus:");
var numThree : number = 2;
var numFour : number = 18;
console.log(numFour % numThree);

// Incrementor
console.log("Incrementor:");
var x : number = 0;

while (x < 10) {
  console.log(x);
  x++;
}

// Decrementor
console.log("Decrementor:");
var x : number = 10;

while (x > 0) {
  console.log(x);
  x--;
}