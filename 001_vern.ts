function greeting(name: string) {
  if (name == "Vern") {
    console.log("Go f**k yourself, " + name);
  } else if (name == "James") {
    console.log("Die in a fire, " + name);
  } else if (name == "Jason") {
    console.log("You're not the worst, " + name);
  } else if (name == "Jordan") {
    console.log(name + " is clearly the coolest");
  } else {
    console.log(name + " isn't terrible, I guess");
  }
}

greeting("Vern");
greeting("James");
greeting("Jason");
greeting("Jordan");
greeting("Brayden");